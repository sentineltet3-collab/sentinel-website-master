    <script>
        // File upload functionality
        document.addEventListener('DOMContentLoaded', function() {
            const fileInput = document.getElementById('applicant-resume');
            const fileNameDisplay = document.querySelector('.file-name');
            const chooseFileBtn = document.querySelector('.choose-file-btn');
            
            // Update file name display when file is selected
            fileInput.addEventListener('change', function() {
                if (this.files.length > 0) {
                    fileNameDisplay.textContent = this.files[0].name;
                    fileNameDisplay.style.color = '#333';
                } else {
                    fileNameDisplay.textContent = 'No file chosen';
                    fileNameDisplay.style.color = '#666';
                }
            });
            
            // Trigger file input when button is clicked
            chooseFileBtn.addEventListener('click', function() {
                fileInput.click();
            });
            
            // Form validation
            const applicationForm = document.querySelector('.application-form');
            if (applicationForm) {
                applicationForm.addEventListener('submit', function(e) {
                    const name = document.getElementById('applicant-name');
                    const email = document.getElementById('applicant-email');
                    const mobile = document.getElementById('applicant-mobile');
                    const position = document.getElementById('applicant-position');
                    const resume = document.getElementById('applicant-resume');
                    
                    let isValid = true;
                    
                    // Clear previous error states
                    clearFormErrors();
                    
                    // Validate required fields
                    if (!name.value.trim()) {
                        showFormError(name, 'Name is required');
                        isValid = false;
                    }
                    
                    if (!email.value.trim()) {
                        showFormError(email, 'Email is required');
                        isValid = false;
                    } else if (!isValidEmail(email.value)) {
                        showFormError(email, 'Please enter a valid email address');
                        isValid = false;
                    }
                    
                    if (!mobile.value.trim()) {
                        showFormError(mobile, 'Mobile number is required');
                        isValid = false;
                    }
                    
                    if (!position.value) {
                        showFormError(position, 'Please select a position');
                        isValid = false;
                    }
                    
                    if (!resume.files.length) {
                        showFormError(resume, 'Please upload your resume');
                        isValid = false;
                    }
                    
                    if (!isValid) {
                        e.preventDefault();
                    }
                });
            }
        });
        
        // Helper functions
        function showFormError(element, message) {
            const errorDiv = document.createElement('div');
            errorDiv.className = 'form-error';
            errorDiv.textContent = message;
            errorDiv.style.color = '#d32f2f';
            errorDiv.style.fontSize = '0.8rem';
            errorDiv.style.marginTop = '0.25rem';
            
            element.parentNode.appendChild(errorDiv);
            element.style.borderColor = '#d32f2f';
        }
        
        function clearFormErrors() {
            const errorMessages = document.querySelectorAll('.form-error');
            errorMessages.forEach(error => error.remove());
            
            const inputs = document.querySelectorAll('.application-form input, .application-form select, .application-form textarea');
            inputs.forEach(input => {
                input.style.borderColor = '#ddd';
            });
        }
        
        function isValidEmail(email) {
            const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
            return emailRegex.test(email);
        }
    </script>
